export function validateFeedbackSchema(feedback: any) {
  const requiredKeys = [
    "overallScore",
    "ATS",
    "toneAndStyle",
    "content",
    "structure",
    "skills",
  ];

  for (const key of requiredKeys) {
    if (!(key in feedback)) {
      throw new Error(`Missing required key: ${key}`);
    }
  }

  return true;
}

